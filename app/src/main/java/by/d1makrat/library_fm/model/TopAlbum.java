package by.d1makrat.library_fm.model;

public class TopAlbum {

    private String mTitle;

    private String mArtistName;

    private String mPlaycount;

    private String mImageUri;

    private String mRank;

    public String getTitle() {
        return mTitle;
    }

    public void setTitle(String mTitle) {
        this.mTitle = mTitle;
    }

    public String getArtistName() {
        return mArtistName;
    }

    public void setArtistName(String mArtistName) {
        this.mArtistName = mArtistName;
    }

    public String getPlaycount() {
        return mPlaycount;
    }

    public void setPlaycount(String mPlaycount) {
        this.mPlaycount = mPlaycount;
    }

    public String getImageUri() {
        return mImageUri;
    }

    public void setImageUri(String mImageUri) {
        this.mImageUri = mImageUri;
    }

    public String getRank() {
        return mRank;
    }

    public void setRank(String mRank) {
        this.mRank = mRank;
    }
}